package Encapsulation;

public class Student 
{
	private String Name;
	private int Rollno;
	private int id;
	
	
	public void setName(String name)
	{
		this.Name=name;
	}
	public String getname()
	{
		return Name;
	}
	
	public void setRollno(int rollno)
	{
		this.Rollno=rollno;
	}
	public int getrollno()
	{
		return Rollno;
	}
	
	public void setid(int id)
	{
		this.id=id;
	}
	public int getid()
	{
		return id;
	}
}
